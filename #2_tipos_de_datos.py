#Tipos de datos:

#Enteros (int):
"""Los números enteros son aquellos que no tienen parte decimal. 
En Python, se representan simplemente escribiendo el número sin comillas ni puntos decimales.
Por ejemplo:
"""
edad = 25
cantidad = 100

#Flotantes (float)
"""
Los números flotantes, también onocidos como números de punto flotante,
son aquellos que tienen una parte decimal. En python, se representan
utilizando un punto para separar la parte entera de la parte decimal.
Por ejemplo:
"""
precio = 9.99
altura = 1.75

#Cadenas de texto (strings)
"""
Las cadenas de texto, o simplemente cadenas, son secuencias de caracteres encerradas
entre commillas simples ('...') o dobles ("..."). Se utilizan para representar
texto en Python.
Por ejemplo:
"""
nombre = "Fulano"
Mensaje = '¡Hola, soy mengano!'

#Booleanos
"""
Los valores booleanos representan los valores de verdad: True y False.
Se utilizan comúnmente en expresiones condicionales y operaciones lógicas.
Por ejemplo:
"""
luz_encendida = True
luz_apagada = False
#En python, los valores booleanos se asignan escribiendo su primera letra en mayúscula
